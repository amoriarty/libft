#include "libft.h"

char		*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*map;

	i = 0;
	map = ft_strnew(42);
	while (s[i] != '\0')
	{
		(*f)(map[i]);
		i++;
	}
	return (map);
}
